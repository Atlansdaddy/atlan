import pw from '/usr/lib/node_modules/playwright/index.js';
const { chromium } = pw;
const b = await chromium.launch();
const page = await b.newPage();
const errs = [];
page.on('pageerror', (e) => errs.push(String(e)));
await page.goto('http://127.0.0.1:4589/');
await page.locator('nav button:has-text("Fleet")').click();
await page.locator('#fleetSubnav button:has-text("Routines")').click();
await page.waitForTimeout(600);
console.log('routines pane visible:', await page.locator('#fp-routines').isVisible());
await page.locator('#routNewBtn').click();
console.log('routine form opens:', await page.locator('#routName').isVisible());
await page.locator('#fleetSubnav button:has-text("Builder")').click();
await page.waitForTimeout(600);
await page.locator('#dPersona summary').click();
console.log('Max persona listed:', await page.locator('#perList').textContent().then((t) => t.includes('Max the Appliance Pro')));
await page.locator('#dCommand summary').click();
console.log('REQUEST_ESTIMATE listed:', await page.locator('#cmdList').textContent().then((t) => t.includes('REQUEST_ESTIMATE')));
console.log('harness var inputs painted:', await page.locator('#hVars [data-v]').count());
// edit command → dynamic rows populate
await page.locator('#cmdList .cedit').first().click();
console.log('var rows on edit:', await page.locator('#varRows .rowedit').count(), '| field rows:', await page.locator('#fieldRows .rowedit').count(), '| checker rows:', await page.locator('#chkRows .rowedit').count());
console.log('page errors:', errs.length ? errs : 'none');
await b.close();
